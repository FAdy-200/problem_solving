from typing import *
from math import prod,factorial


class Solution:
    def __init__(self):
        self.d = set()
        self.nums = None
        self.k = None
        self.ans = 0

    def helper(self, s, i, a):
        if i == len(self.nums):
            t = 1 if prod(s) < self.k else 0
            if s not in self.d:
                self.ans += t
                self.d.add(s)
            return
        if prod(s) < self.k:
            self.helper(s + (self.nums[i],), i + 1, 1)
            self.helper(s, i + 1, 1)

    def numSubseqProductLessThanK(self, nums: List[int], k: int) -> int:
        self.nums = nums
        self.k = k
        self.helper((), 0, 0)
        return self.ans

    def numSubarrayProductLessThanK(self, nums: List[int], k: int) -> int:
        ans = 0
        i = 0
        j = 0
        if prod(nums) < k:
            return factorial(len(nums))
        while i < len(nums):
            if prod(nums[i:j + 1]) < k:
                ans += 1
            else:
                i += 1
                j = i
                continue
            j += 1
            if j == len(nums):
                i += 1
                j = i
        return ans


